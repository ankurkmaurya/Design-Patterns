package com.ankurmaurya.creational;

/**
 * 
 * <h1>Creational design patterns</h1>
 * 
 * 
 * <h3>Points :</h3>
 * <ul>
 * <li>Creational design patterns provide solution to instantiate a object in
 * the best possible way for specific situations.</li>
 * <li>These design patterns are all about class instantiation. This pattern can
 * be further divided into class-creation patterns and object-creational
 * patterns. While class-creation patterns use inheritance effectively in the
 * instantiation process, object-creation patterns use delegation effectively to
 * get the job done.</li>
 * </ul>
 * 
 * <h3>Various Creational Design Patterns</h3>
 * <ol>
 * <li><b>Abstract Factory : </b>Creates an instance of several families of
 * classes</li>
 * <li><b>Builder : </b>Separates object construction from its
 * representation</li>
 * <li><b>Factory Method : </b>Creates an instance of several derived
 * classes</li>
 * <li><b>Prototype : </b>A fully initialized instance to be copied or
 * cloned</li>
 * <li><b>Singleton : </b>A class of which only a single instance can
 * exist.</li>
 * </ol>
 * 
 * 
 * @see com.ankurmaurya.creational.abstractfactory.AbstractFactory 1. Abstract Factory
 * @see com.ankurmaurya.creational.builder.Builder 2. Builder
 * @see com.ankurmaurya.creational.factorymethod.FactoryMethod 3. Factory Method
 * @see com.ankurmaurya.creational.prototype.Prototype 4. Prototype
 * @see com.ankurmaurya.creational.singleton.Singleton 5. Singleton
 * 
 * 
 * @author Ankur Maurya
 * 
 */

